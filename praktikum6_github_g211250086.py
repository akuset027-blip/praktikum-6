import math

def euclidean_distance(x, y):
    # Pastikan dimensi sama
    if len(x) != len(y):
        return "Dimensi tidak sama"

    total = 0

    # Hitung jumlah kuadrat selisih
    for i in range(len(x)):
        selisih = x[i] - y[i]
        total += selisih * selisih

    # Akar kuadrat
    hasil = math.sqrt(total)

    return hasil


